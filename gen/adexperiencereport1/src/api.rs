    pub fn param<T>(mut self, name: T, value: T) -> ViolatingSiteListCall<'a, C>
    where
        T: AsRef<str>,
    {
        self._additional_params
            .insert(name.as_ref().to_string(), value.as_ref().to_string());
        self
    }
