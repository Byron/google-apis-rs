///
/// # Examples
///
/// Instantiate a new hub
///
/// ```test_harness,no_run
/// extern crate hyper;
/// extern crate hyper_rustls;
/// extern crate google_tpu1_alpha1 as tpu1_alpha1;
/// use tpu1_alpha1::api::Node;
/// use tpu1_alpha1::{Result, Error};
/// # async fn dox() {
