#[derive(Clone)]
pub struct AppState<S> {
    pub client: hyper::Client<S, hyper::body::Body>,
    pub auth: Box<dyn client::Authy>,
    _user_agent: String,
    _base_url: String,
    _root_url: String,
}

impl<'a, S> client::Hub for AppState<S> {}

impl<'a, S> AppState<S> {

    pub fn new<A: client::Authy>(client: hyper::Client<S, hyper::body::Body>, auth: A) -> AppState<S> {
        AppState {
            client,
            auth: Box::new(auth),
            _user_agent: "google-api-rust-client/4.0.1".to_string(),
            _base_url: "https://www.googleapis.com/appstate/v1/".to_string(),
            _root_url: "https://www.googleapis.com/".to_string(),
        }
    }

    pub fn states(&'a self) -> StateMethods<'a, S> {
        StateMethods { hub: &self }
    }

    /// Set the user-agent header field to use in all requests to the server.
    /// It defaults to `google-api-rust-client/4.0.1`.
    ///
    /// Returns the previously set user-agent.
    pub fn user_agent(&mut self, agent_name: String) -> String {
        mem::replace(&mut self._user_agent, agent_name)
    }

    /// Set the base url to use in all requests to the server.
    /// It defaults to `https://www.googleapis.com/appstate/v1/`.
    ///
    /// Returns the previously set base url.
    pub fn base_url(&mut self, new_base_url: String) -> String {
        mem::replace(&mut self._base_url, new_base_url)
    }

    /// Set the root url to use in all requests to the server.
    /// It defaults to `https://www.googleapis.com/`.
    ///
    /// Returns the previously set root url.
    pub fn root_url(&mut self, new_root_url: String) -> String {
        mem::replace(&mut self._root_url, new_root_url)
    }
}


