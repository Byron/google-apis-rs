pub struct ArchiveInsertCall<'a, C, A>
    where C: 'a, A: 'a {

    hub: &'a GroupsMigration<C, A>,
    _group_id: String,
    _delegate: Option<&'a mut dyn client::Delegate>,
    _additional_params: HashMap<String, String>,
    _scopes: BTreeMap<String, ()>
}

