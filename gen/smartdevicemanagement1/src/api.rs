pub enum Scope {
    /// See and/or control the devices that you selected
    SdmService,
}

impl AsRef<str> for Scope {
    fn as_ref(&self) -> &str {
        match *self {
            Scope::SdmService => "https://www.googleapis.com/auth/sdm.service",
        }
    }
}

#[allow(clippy::derivable_impls)]
impl Default for Scope {
    fn default() -> Scope {
        Scope::SdmService
    }
}

