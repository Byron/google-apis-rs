/// It is not used directly, but through the [`CloudAsset`] hub.
///
/// # Example
///
/// Instantiate a resource builder
///
/// ```test_harness,no_run
/// extern crate hyper;
/// extern crate hyper_rustls;
/// extern crate google_cloudasset1_beta1 as cloudasset1_beta1;
